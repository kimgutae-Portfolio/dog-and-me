create or replace function public.create_memory_order(p_data jsonb)
returns table(order_id uuid, order_number text, quoted_price integer)
language plpgsql
security definer set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_order_id uuid := gen_random_uuid();
  v_order_number text;
  v_quoted_price integer;
  v_campaign_id text;
  v_launch_count integer;
begin
  if v_user_id is null then raise exception 'authentication required'; end if;
  if coalesce(trim(p_data ->> 'pet_name'), '') = '' then raise exception 'pet name required'; end if;
  if coalesce(trim(p_data ->> 'breed'), '') = '' then raise exception 'breed required'; end if;

  perform pg_advisory_xact_lock(hashtext('wan-memory-launch-monitor-10'));
  select count(*) into v_launch_count
  from public.orders
  where campaign_id = 'launch-monitor-10' and status <> 'cancelled';

  if v_launch_count < 10 then
    v_quoted_price := 24800;
    v_campaign_id := 'launch-monitor-10';
  else
    v_quoted_price := 29800;
    v_campaign_id := null;
  end if;

  v_order_number := 'WM-' || to_char(now(), 'YYYY') || '-' || upper(substr(replace(v_order_id::text, '-', ''), 1, 6));

  insert into public.orders (
    id, user_id, order_number, pet_name, name_kana, breed, age_text,
    purpose, personality, first_meeting, favorite_memory, message_to_pet,
    avoid_notes, style, aspect_ratio, narration, bgm, quoted_price,
    regular_price, campaign_id, status
  ) values (
    v_order_id, v_user_id, v_order_number, trim(p_data ->> 'pet_name'),
    nullif(trim(p_data ->> 'name_kana'), ''), trim(p_data ->> 'breed'),
    nullif(trim(p_data ->> 'age_text'), ''), p_data ->> 'purpose',
    coalesce(p_data -> 'personality', '[]'::jsonb),
    nullif(trim(p_data ->> 'first_meeting'), ''),
    nullif(trim(p_data ->> 'favorite_memory'), ''),
    nullif(trim(p_data ->> 'message_to_pet'), ''),
    nullif(trim(p_data ->> 'avoid_notes'), ''),
    p_data ->> 'style', p_data ->> 'aspect_ratio', p_data ->> 'narration',
    p_data ->> 'bgm', v_quoted_price, 29800, v_campaign_id, 'awaiting_materials'
  );

  insert into public.order_events (order_id, actor_id, event_type, payload)
  values (v_order_id, v_user_id, 'order_created', jsonb_build_object('quoted_price', v_quoted_price));

  return query select v_order_id, v_order_number, v_quoted_price;
end;
$$;

revoke all on function public.create_memory_order(jsonb) from public;
grant execute on function public.create_memory_order(jsonb) to authenticated;

create or replace function public.get_memory_film_pricing()
returns table(
  current_price integer,
  regular_price integer,
  launch_limit integer,
  launch_used integer,
  launch_remaining integer,
  campaign_active boolean
)
language sql
stable
security definer set search_path = public
as $$
  with counts as (
    select count(*)::integer as used
    from public.orders
    where campaign_id = 'launch-monitor-10' and status <> 'cancelled'
  )
  select
    case when used < 10 then 24800 else 29800 end,
    29800,
    10,
    used,
    greatest(10 - used, 0),
    used < 10
  from counts;
$$;

revoke all on function public.get_memory_film_pricing() from public;
grant execute on function public.get_memory_film_pricing() to anon, authenticated;

create or replace function public.submit_memory_order(p_order_id uuid)
returns void
language plpgsql
security definer set search_path = public
as $$
begin
  update public.orders
  set status = 'materials_submitted', stage_updated_at = now()
  where id = p_order_id and user_id = auth.uid() and status = 'awaiting_materials';
  if not found then raise exception 'order not found or cannot be submitted'; end if;
  insert into public.order_events(order_id, actor_id, event_type)
  values (p_order_id, auth.uid(), 'materials_submitted');
end;
$$;

revoke all on function public.submit_memory_order(uuid) from public;
grant execute on function public.submit_memory_order(uuid) to authenticated;

create or replace function public.select_memory_concept(p_order_id uuid, p_slot text)
returns void
language plpgsql
security definer set search_path = public
as $$
begin
  if p_slot not in ('A', 'B') then raise exception 'invalid concept slot'; end if;
  if not exists (
    select 1 from public.concepts
    where order_id = p_order_id and slot = p_slot and status = 'published'
  ) then raise exception 'concept not available'; end if;

  update public.orders
  set selected_concept_slot = p_slot, status = 'concept_selected', stage_updated_at = now()
  where id = p_order_id and user_id = auth.uid() and status in ('concepts_ready', 'concept_selected');
  if not found then raise exception 'order not found or concept cannot be selected'; end if;

  insert into public.order_events(order_id, actor_id, event_type, payload)
  values (p_order_id, auth.uid(), 'concept_selected', jsonb_build_object('slot', p_slot));
end;
$$;

revoke all on function public.select_memory_concept(uuid, text) from public;
grant execute on function public.select_memory_concept(uuid, text) to authenticated;

create or replace function public.request_order_revision(p_order_id uuid, p_category text, p_body text)
returns void
language plpgsql
security definer set search_path = public
as $$
begin
  if coalesce(trim(p_body), '') = '' then raise exception 'revision body required'; end if;
  if not exists (
    select 1 from public.orders
    where id = p_order_id and user_id = auth.uid() and status in ('customer_review', 'delivered')
  ) then raise exception 'order not found or revision unavailable'; end if;

  insert into public.revision_requests(order_id, user_id, category, body)
  values (p_order_id, auth.uid(), coalesce(nullif(trim(p_category), ''), 'その他'), trim(p_body));

  update public.orders
  set status = 'revision_requested', stage_updated_at = now()
  where id = p_order_id;

  insert into public.order_events(order_id, actor_id, event_type, payload)
  values (p_order_id, auth.uid(), 'revision_requested', jsonb_build_object('category', p_category));
end;
$$;

revoke all on function public.request_order_revision(uuid, text, text) from public;
grant execute on function public.request_order_revision(uuid, text, text) to authenticated;

