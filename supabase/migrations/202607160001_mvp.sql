create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  full_name text,
  role text not null default 'customer' check (role in ('customer', 'admin')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.orders (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  order_number text not null unique,
  pet_name text not null,
  name_kana text,
  breed text not null,
  age_text text,
  purpose text not null check (purpose in ('いまを残す', '虹の橋メモリアル')),
  personality jsonb not null default '[]'::jsonb,
  first_meeting text,
  favorite_memory text,
  message_to_pet text,
  avoid_notes text,
  style text not null,
  aspect_ratio text not null,
  narration text not null,
  bgm text not null,
  status text not null default 'awaiting_materials' check (status in (
    'awaiting_materials', 'materials_submitted', 'reviewing_materials',
    'concepts_ready', 'concept_selected', 'production', 'customer_review',
    'revision_requested', 'quality_check', 'delivered', 'cancelled'
  )),
  payment_status text not null default 'pending' check (payment_status in ('pending', 'invoice_sent', 'paid', 'refunded')),
  quoted_price integer not null,
  regular_price integer not null default 29800,
  currency text not null default 'JPY',
  campaign_id text,
  selected_concept_slot text check (selected_concept_slot in ('A', 'B')),
  due_date date,
  admin_notes text,
  stage_updated_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists orders_user_created_idx on public.orders(user_id, created_at desc);
create index if not exists orders_status_idx on public.orders(status, created_at desc);

create table if not exists public.assets (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.orders(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  category text not null check (category in ('source_image', 'source_video', 'final_video', 'thumbnail')),
  storage_path text not null unique,
  original_filename text not null,
  mime_type text not null,
  file_size bigint not null check (file_size >= 0),
  created_at timestamptz not null default now()
);

create index if not exists assets_order_idx on public.assets(order_id, created_at);

create table if not exists public.concepts (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.orders(id) on delete cascade,
  slot text not null check (slot in ('A', 'B')),
  title text not null,
  tone text not null,
  summary text not null,
  scenes jsonb not null default '[]'::jsonb,
  status text not null default 'draft' check (status in ('draft', 'published')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique(order_id, slot)
);

create table if not exists public.revision_requests (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.orders(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  category text not null,
  body text not null,
  status text not null default 'open' check (status in ('open', 'resolved')),
  created_at timestamptz not null default now(),
  resolved_at timestamptz
);

create index if not exists revision_requests_order_idx on public.revision_requests(order_id, created_at desc);

create table if not exists public.messages (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.orders(id) on delete cascade,
  sender_id uuid not null references public.profiles(id) on delete cascade,
  body text not null check (char_length(body) between 1 and 3000),
  created_at timestamptz not null default now()
);

create index if not exists messages_order_idx on public.messages(order_id, created_at);

create table if not exists public.deliveries (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null unique references public.orders(id) on delete cascade,
  final_asset_id uuid not null references public.assets(id) on delete restrict,
  title text not null,
  customer_message text,
  delivered_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.order_events (
  id bigint generated by default as identity primary key,
  order_id uuid not null references public.orders(id) on delete cascade,
  actor_id uuid references public.profiles(id) on delete set null,
  event_type text not null,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

